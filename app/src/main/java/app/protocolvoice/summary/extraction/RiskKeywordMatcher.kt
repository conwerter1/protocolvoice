package app.protocolvoice.summary.extraction

/**
 * Поиск предложений с триггер-словами проблем/рисков.
 *
 * Расширенный список покрывает типичные аудиторские сигналы:
 *  - Явные формулировки: "проблема", "риск", "нарушение"
 *  - Отрицания: "не успева", "не работа", "никак не", "никто"
 *  - Последствия: "штраф", "санкции", "конфликт"
 *  - Эмоциональные маркеры: "впервые", "опять", "пофиг"
 *  - Конфликты/непонимание: "не понима", "не объясня", "обман"
 *
 * Снижен минимум слов (с 10 до 5) — в разговорной речи риски часто
 * выражаются короткими репликами. Но введён скоринг — предложения с несколькими
 * триггерами идут выше.
 *
 * Возвращает список предложений с указанием, какие триггеры в них сработали —
 * это даёт прозрачность пользователю (видно почему предложение попало в "риски").
 */
object RiskKeywordMatcher {

    val DEFAULT_TRIGGERS: List<String> = listOf(
        // === Явные ===
        "проблем", "риск",
        "нарушени", "несоответств", "наруш", "ошибк",
        "замечани", "претензи", "жалоб",
        // === Негативные глаголы ===
        "не успева", "не работа", "не подпис", "не соглас",
        "не дают", "не дал", "не разреш", "мешают", "блокиру",
        "не приня", "не выполн", "не включ", "не учтен",
        "не получ", "не предост", "не подтверж",
        // === Непонимание / неясность ===
        "не понима", "не понял", "не ясн", "непонят",
        "не объясн", "не разъяс", "путан", "неразбер",
        // === Никто/ничего/никак ===
        "никак не", "никаких", "никто", "никого",
        "ничего не", "ничего не сооб",
        // === Финансовые/юридические ===
        "штраф", "санкц", "пеня", "взыск",
        "конфликт", "спор", "суд", "иск",
        "убыток", "убытк", "потерял", "потери",
        // === Качество/безопасность ===
        "авари", "обман", "некачеств", "неисправ",
        "сомнен", "скрыт", "обход",
        "фальсиф", "подлог", "приписк", "поддел",
        // === Сроки ===
        "задержк", "опаздыва", "просрочк", "отстава",
        "сорвал", "сорван", "срыв",
        // === Утаивание ===
        "скрыл", "не сообщил", "не предостав", "не дал",
        "умалчив", "утаил", "утаив",
        // === Эмоциональные маркеры (важны в разговорных интервью) ===
        "впервые", "всё равно", "пофиг", "похрен",
        "хренов", "тупой", "дурацк", "безобраз",
        "кошмар", "бардак", "беспоряд",
        // === Аудиторская специфика ===
        "приказ", "распоряжен", "ответствен", "закреплён",
        "проверк", "ревиз", "аудит",
        "выискать", "ищем", "не можем найти",
    )

    data class RiskMatch(
        val sentence: String,
        val triggers: List<String>,
        /** Позиция предложения в исходном списке (для сортировки). */
        val position: Int,
        /** Скор важности (больше триггеров = больше важность). */
        val score: Int = triggers.size,
    )

    /**
     * Найти предложения с триггерами. Сортировка по скору (важность выше).
     *
     * @param sentences список предложений (порядок исходного текста)
     * @param triggers пользовательский список триггеров (по умолчанию [DEFAULT_TRIGGERS])
     * @param minWords минимальная длина предложения в словах (снижен до 5 — иначе теряем короткие реплики)
     * @param maxWords максимальная (слишком длинные предложения нечитаемы в отчёте)
     * @param maxResults ограничение результатов
     */
    fun findRiskSentences(
        sentences: List<String>,
        triggers: List<String> = DEFAULT_TRIGGERS,
        minWords: Int = 5,
        maxWords: Int = 80,
        maxResults: Int = 20,
    ): List<RiskMatch> {
        val all = mutableListOf<RiskMatch>()
        val triggersLower = triggers.map { it.lowercase() }
        for ((i, s) in sentences.withIndex()) {
            val wc = s.split(Regex("\\s+")).size
            if (wc < minWords || wc > maxWords) continue
            val sl = s.lowercase()
            val matched = triggersLower.filter { it in sl }
            if (matched.isNotEmpty()) {
                all.add(RiskMatch(s, matched.distinct(), i))
            }
        }
        // Сортируем по скору (важность) → потом по позиции (раньше в тексте — приоритет).
        return all.sortedWith(
            compareByDescending<RiskMatch> { it.score }.thenBy { it.position },
        ).take(maxResults)
    }
}
